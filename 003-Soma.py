# Crie um programa que leia dois números pelo teclado e mostre a soma entre eles.

num_1 = int(input('Digite um número: '))
num_2 = int(input('Digite outro número: '))
soma = (num_1 + num_2)
print(f'    {num_1}')
print(f'   +{num_2}')
print(f'  _____')
print(f'    {soma}')
print(f'A soma de {num_1} e {num_2} é {soma}')