# Desenvolva um programa que leia duas notas de um aluno, calcule e mostre a sua média.

alu_nt_1 = float(input('Digite o valor da primeira nota: '))
alu_nt_2 = float(input('Digite a segunda nota: '))
media = (alu_nt_1 + alu_nt_2) / 2
print(f'A média entre as notas {alu_nt_1} e {alu_nt_2} do aluno é de {media}')
