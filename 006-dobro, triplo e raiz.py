# Crie um programa que leia um número e moste o seu dobro, triplo e a raiz quadrada.

num = int(input('Digite um número: '))
dobro_num = (num * 2)
triplo_num = (num * 3)
raiz_num = (num ** (1/2))
print(f'O dobro de {num} é {dobro_num}')
print(f'O triplo de {num} é {triplo_num}')
print(f'A raiz quadrada de {num} é {raiz_num}')
